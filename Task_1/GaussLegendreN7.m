function res = GaussLegendreN7(f, a, b, m)
  x8_ = [-0.96028986, -0.79666648, -0.52553142, -0.18343464, 0.18343464, 0.52553142, 0.79666648, 0.96028986];
  a8_ = [0.10122854 ,  0.22238104,  0.31370664,  0.36268378, 0.36268378, 0.31370664, 0.22238104, 0.10122854];
  h = (b-a)/m;
  res = h./2*sum(sum((@(j, k) a8_(k).*f(a + j.*h + h./2 + h./2 .*x8_(k)))([0:m-1]', [1:8])));
endfunction